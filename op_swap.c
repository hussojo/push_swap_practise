#include "push_swap.h"

void ft_op_sa(int *st_a)
{
	int temp;
	int i;

	temp = 0;
	i = 0;
	temp = st_a[i];
	st_a[i] = st_a[i + 1];
	st_a[i + 1] = temp;
	ft_putstr_fd("sa\n", 1);
	return ;
}
